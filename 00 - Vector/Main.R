# Ini artinya kamu telah membuat dan menampilkan sebuah vector yang berisi tiga 3 angka: 4, 5 dan 6. Terlihat tiga angka tersebut semua ditampilkan dalam satu baris.
# Sekarang kita akan simpan vector ini ke dalam variable angka dan ditampilkan dengan menggunakan fungsi print pada bagian bawah comment "# Variable bernama angka dengan input berupa vector".


> # Ini adalah contoh vector untuk angka numerik dengan 3 data c(4, 5, 6)
> c(4,5,6)
[1] 4 5 6

> # Variable bernama angka dengan input berupa vector
> angka <- c(4,5,6) 

> # Tampilkan isi variable angka dengan fungsi print
> print(angka)
[1] 4 5 6

# Terlihat bahwa pada baris terakhir, variable angka yang kita tampilkan sama seperti tampilan awal yang menunjukkan angka-angka ini disimpan dalam bentuk vector.